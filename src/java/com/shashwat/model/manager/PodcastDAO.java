package com.shashwat.model.manager;

/**
 *
 * @author HP
 */
public class PodcastDAO {

    private int podcastId,likes;
    private String title,hostName,category,releaseDate,description,timeDuration,language,
            videoUrl,thumbnail;

    public PodcastDAO(int podcastId, int likes, String title, String hostName, String category, String releaseDate, String description, String timeDuration, String language, String videoUrl, String thumbnail) {
        this.podcastId = podcastId;
        this.likes = likes;
        this.title = title;
        this.hostName = hostName;
        this.category = category;
        this.releaseDate = releaseDate;
        this.description = description;
        this.timeDuration = timeDuration;
        this.language = language;
        this.videoUrl = videoUrl;
        this.thumbnail = thumbnail;
    }

    public int getPodcastId() {
        return podcastId;
    }

    public void setPodcastId(int podcastId) {
        this.podcastId = podcastId;
    }

    public int getLikes() {
        return likes;
    }

    public void setLikes(int likes) {
        this.likes = likes;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getHostName() {
        return hostName;
    }

    public void setHostName(String hostName) {
        this.hostName = hostName;
    }

    public String getCategory() {
        return category;
    }

    public void setCategory(String category) {
        this.category = category;
    }

    public String getReleaseDate() {
        return releaseDate;
    }

    public void setReleaseDate(String releaseDate) {
        this.releaseDate = releaseDate;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getTimeDuration() {
        return timeDuration;
    }

    public void setTimeDuration(String timeDuration) {
        this.timeDuration = timeDuration;
    }

    public String getLanguage() {
        return language;
    }

    public void setLanguage(String language) {
        this.language = language;
    }

    public String getVideoUrl() {
        return videoUrl;
    }

    public void setVideoUrl(String videoUrl) {
        this.videoUrl = videoUrl;
    }

    public String getThumbnail() {
        return thumbnail;
    }

    public void setThumbnail(String thumbnail) {
        this.thumbnail = thumbnail;
    }

    public PodcastDAO() {
    }
    
}
